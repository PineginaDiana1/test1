public class BookLesson7 {

    public String name;
    public int year;

    public BookLesson7(String name, int year) {
        this.name = name;
        this.year = year;
    }

    @Override
    public String toString() {
        return "BookLesson7{" +
                "name='" + name + '\'' +
                ", year=" + year +
                '}';
    }
}
